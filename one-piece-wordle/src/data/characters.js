//Funciones Auxiliares

const API_URL = 'https://api-onepiece.com/api/characters';

const normalizeCharacter = (rawCharacter) => {
    if (!rawCharacter || typeof rawCharacter !== 'object') return null;

    const name = rawCharacter.name || rawCharacter.fullName || rawCharacter.full_name || rawCharacter.character_name || rawCharacter.title || '';
    const fullName = rawCharacter.fullName || rawCharacter.full_name || rawCharacter.fullname || name;
    const affiliation = rawCharacter.affiliation || rawCharacter.crew?.name || rawCharacter.affiliations?.[0] || rawCharacter.organization || '';
    const bounty = rawCharacter.bounty || rawCharacter.bountyValue || rawCharacter.bounty_value || rawCharacter.reward || '';
    const fruit = rawCharacter.fruit || rawCharacter.devilFruit || rawCharacter.devil_fruit || rawCharacter.fruitName || null;
    const haki = rawCharacter.haki || rawCharacter.hakiTypes || rawCharacter.haki_type || null;
    const description = rawCharacter.description || rawCharacter.about || rawCharacter.bio || rawCharacter.summary || '';
    const image = rawCharacter.image || rawCharacter.img || rawCharacter.picture || rawCharacter.imageUrl || rawCharacter.image_url || '';

    return {
        id: rawCharacter.id || rawCharacter.character_id || rawCharacter.slug || `${name}-${Math.random()}`,
        name,
        fullName,
        affiliation,
        bounty,
        haki,
        fruit,
        description,
        image
    };
};

export const loadCharacters = async () => {
    try {
        const response = await fetch(API_URL, {
            headers: {
                Accept: 'application/json'
            }
        });

        if (!response.ok) {
            throw new Error('No se pudo cargar la API');
        }

        const data = await response.json();
        const payload = Array.isArray(data)
            ? data
            : Array.isArray(data?.characters)
                ? data.characters
                : Array.isArray(data?.results)
                    ? data.results
                    : Array.isArray(data?.data)
                        ? data.data
                        : [];

        const normalizedCharacters = payload
            .map((item) => normalizeCharacter(item))
            .filter(Boolean);

        if (normalizedCharacters.length >= 20) {
            return normalizedCharacters;
        }

        return normalizedCharacters.length > 0 ? normalizedCharacters : characters;
    } catch (error) {
        console.warn('Usando personajes locales por fallback:', error);
        return characters;
    }
};

// Funcion para pj aleatorio :D!
export const getRandomCharacter = (pool = characters) => {
    const randomIndex = Math.floor(Math.random() * pool.length);
    return pool[randomIndex] || characters[0];
};

// Funcion para obtener el personaje del día basado en fecha :)!
export const getCharacterOfTheDay = (pool = characters) => {
    if (!Array.isArray(pool) || pool.length === 0) {
        return characters[0];
    }

    const today = new Date();
    const dateKey = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
    const hash = Array.from(dateKey).reduce((acc, char) => {
        return (acc * 31 + char.charCodeAt(0)) % 1000003;
    }, 0);

    const index = hash % pool.length;
    return pool[index] || pool[0];
};
